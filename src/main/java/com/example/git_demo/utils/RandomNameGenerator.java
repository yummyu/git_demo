    private static final Random random = new Random();

    // 生成单个随机姓名
    public static String generateRandomName() {
        // 随机选择姓氏
        String surname = SURNAMES[random.nextInt(SURNAMES.length)];

        // 随机选择性别（0男，1女，2中性）
        int gender = random.nextInt(3);

        StringBuilder fullName = new StringBuilder(surname);

        switch (gender) {
            case 0: // 男性
                fullName.append(MALE_NAMES[random.nextInt(MALE_NAMES.length)]);
                break;
            case 1: // 女性
                fullName.append(FEMALE_NAMES[random.nextInt(FEMALE_NAMES.length)]);
                break;
            default: // 中性
                fullName.append(UNISEX_NAMES[random.nextInt(UNISEX_NAMES.length)]);
        }


        return fullName.toString();
    }

    // 生成指定数量的随机姓名
    public static List<String> generateRandomNames(int count) {
        List<String> names = new ArrayList<>();
        for (int i = 0; i < count; i++) {
            names.add(generateRandomName());
        }
        return names;
    }
